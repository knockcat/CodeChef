#include <iostream>
using namespace std;

int main() {
	// KNOCKCAT
	
	int t;
	cin >> t;
	
	while(t--)
	{
	    int x, y;
	    cin >> x >> y;
	    
	   if(x/2 <= y)
	        cout<<x/2<<endl;
	   else if(x >= y*2)
	        cout<<y<<endl;
	        
	    
	    
	}
	return 0;
}

